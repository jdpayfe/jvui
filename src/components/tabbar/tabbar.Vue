<template>
    <div class="jv-tabbar jv-hairline--top-bottom" :class="{'jv-tabbar--fixed': fixed}">
        <slot />
    </div>
</template>

<script>
export default{
    name: 'jv-tabbar',

    data () {
        return {
            items: []
        };
    },
    props: {
        value: Number,
        fixed: {
            type: Boolean,
            default: true
        }
    },
    watch: {
        items () {
            this.setActiveItem();
        },
        value () {
            this.setActiveItem();
        }
    },
    methods: {
        setActiveItem () {
            this.items.forEach((item, index) => {
                item.active = index === this.value;
            });
        },
        onChange (active) {
            this.$emit('input', active);
            this.$emit('change', active);
        }
    }
};
</script>
